#slicing = create a substring by extracting elements from another string indexing [] or slice()
# [start:stop:step]

name = "Bro Code"


first_name = name[:3]
last_name = name[4:]
funky_name = name[::3]
reversed_name = name[::-1]

print(first_name)
print(last_name)
print(funky_name)
print(reversed_name)
